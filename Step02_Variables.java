public class Step02_Variables {
    public static void main(String[] args) {
        int x = 7;
        int y = 3;

        int sum = x + y;
        int difference = x - y;
        int product = x * y;
        int quotient = x / y;

        System.out.println("足し算 : " + sum);
        System.out.println("引き算 : " + difference);
        System.out.println("掛け算 : " + product);
        System.out.println("割り算 : " + quotient);
    }
}
